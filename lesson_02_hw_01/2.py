""" Завдання 2.
З допомогою Python порахувати наступні вирази для a=1, b=5, c=4."""


a = float(input('Введить число а: '))
b = float(input('Введить число b: '))
c = float(input('Введить число c: '))
print('x1 =', (-b + (b**2 - 4 * a * c)**0.5)/(2 * a))
print('x2 =', (-b - (b**2 - 4 * a * c)**0.5)/(2 * a))
